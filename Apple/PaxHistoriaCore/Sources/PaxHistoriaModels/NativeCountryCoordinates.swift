        let normalized = code.uppercased()
        let pair = centroids[normalized] ?? distributedFallback(for: normalized)
        return CLLocationCoordinate2D(latitude: pair.0, longitude: pair.1)
    }

    private static func distributedFallback(for code: String) -> (Double, Double) {
        let scalars = Array(code.unicodeScalars.map(\.value))
        let seed = scalars.enumerated().reduce(UInt32(2_166_136_261)) { value, item in
            (value ^ UInt32(item.element + UInt32(item.offset * 31))) &* 16_777_619
        }
        let latitude = Double(Int(seed % 120) - 60)
        let longitude = Double(Int((seed / 120) % 320) - 160)
        return (latitude, longitude)
    }

